
public class Point2D {
    // stores two points
    private float x;
    private float y;

    public Point2D(float x, float y) {
        this.x = x;
        this.y = y;
    }

    // setters
    
    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

    // getters

    public float getX() {
        return this.x;
    }

    public float getY() {
        return this.y;
    }
}
